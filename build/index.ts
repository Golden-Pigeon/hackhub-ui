/**
 * build/index.ts — Build pipeline
 *
 * Transforms a multi-file TSX component tree into the single-file app.html
 * that HackHub's mod loader expects.
 *
 * Pipeline:
 *   1. Discover apps under src/apps/{name}/app.ts
 *   2. Bundle each app entry → IIFE JS string (framework + components inlined)
 *   3. Inject JS + shared CSS → app.html
 *   4. Generate @RegisterApp declarations → src/generated/apps.ts
 *   5. Delegate to standard @hotbunny/hackhub-content-sdk/build → dist/mod.js
 */

import * as fs from "node:fs";
import * as path from "node:path";

interface AppEntry {
  name: string; title: string; icon: string;
  entry: string;      // abs path to app.ts
  size: { width: number; height: number };
  unlocked: boolean;
}

// ── App discovery ─────────────────────────────────────────────────────

function discoverApps(root: string): AppEntry[] {
  const ad = path.join(root, "src", "apps");
  if (!fs.existsSync(ad)) return [];
  const apps: AppEntry[] = [];
  for (const d of fs.readdirSync(ad, { withFileTypes: true })) {
    if (!d.isDirectory()) continue;
    const e = path.join(ad, d.name, "app.ts");
    if (!fs.existsSync(e)) continue;
    apps.push({
      name: d.name, title: d.name, icon: "assets/icon.svg",
      entry: e, size: { width: 600, height: 440 }, unlocked: true,
    });
  }
  return apps;
}

// ── Bundle app → JS string ───────────────────────────────────────────

async function bundleApp(root: string, app: AppEntry): Promise<string> {
  const esbuild = await import("esbuild");
  const result = await esbuild.build({
    entryPoints: [app.entry],
    bundle: true, write: false, format: "iife",
    platform: "browser", target: "es2020",
    minify: !process.argv.includes("--no-minify"),
    sourcemap: false, legalComments: "none",
    jsx: "transform", jsxFactory: "h", jsxFragment: "Fragment",
    loader: { ".tsx": "tsx", ".ts": "ts" },
  });
  return result.outputFiles[0].text;
}

// ── HTML template ────────────────────────────────────────────────────

function generateHTML(css: string, js: string): string {
  return `<!DOCTYPE html>
<html><head><meta charset="utf-8"/><style>${css}</style></head>
<body><div id="root"></div><script>${js}</script></body></html>`;
}

// ── Generate @RegisterApp declarations ────────────────────────────────

function generateAppsModule(apps: { entry: AppEntry; html: string }[]): string {
  const lines = [
    "// AUTO-GENERATED by hh-ui/build — do not edit.",
    'import { App, RegisterApp } from "@hotbunny/hackhub-content-sdk";',
    "",
  ];
  apps.forEach(({ entry, html }, i) => {
    const h = JSON.stringify(html);
    const cls = `App${i}_${entry.name.replace(/[^a-zA-Z0-9]/g, "_")}`;
    lines.push(
      `@RegisterApp`, `class ${cls} extends App {`,
      `    AppName = ${JSON.stringify(entry.name)};`,
      `    Title = ${JSON.stringify(entry.title)};`,
      `    Icon = ${JSON.stringify(entry.icon)};`,
      `    HTML = ${h};`,
      `    DefaultSize = ${JSON.stringify(entry.size)};`,
      `    Unlocked = ${JSON.stringify(entry.unlocked)};`,
      `    Store = { title: ${JSON.stringify(entry.title)}, ratings: 5,`,
      `              description: "Built with hh-ui." };`,
      `}`, "",
    );
  });
  lines.push("export const __reg = true;");
  return lines.join("\n");
}

// ── CSS extraction ───────────────────────────────────────────────────

async function loadCSS(root: string): Promise<string> {
  const cssFile = path.join(root, "src", "styles.ts");
  if (!fs.existsSync(cssFile)) return "";
  try {
    const m = await import(cssFile);
    return typeof m.APP_CSS === "string" ? m.APP_CSS : "";
  } catch { return ""; }
}

// ── Main entry ───────────────────────────────────────────────────────

export async function buildMod(root?: string) {
  root = root ?? process.cwd();
  const tag = "\x1b[36m[hh-ui]\x1b[0m";

  // 1. Discover
  const apps = discoverApps(root);
  if (!apps.length) { console.error(`${tag} No apps found`); process.exit(1); }

  // 2. Bundle each app
  const css = await loadCSS(root);
  const generated: { entry: AppEntry; html: string }[] = [];
  for (const a of apps) {
    const js = await bundleApp(root, a);
    const html = generateHTML(css, js);
    generated.push({ entry: a, html });
    console.log(`${tag} ${a.name}: ${(html.length / 1024).toFixed(1)} KB`);
  }

  // 3. Write generator module
  const genDir = path.join(root, "src", "generated");
  fs.mkdirSync(genDir, { recursive: true });
  fs.writeFileSync(path.join(genDir, "apps.ts"), generateAppsModule(generated), "utf-8");

  // 4. Delegate to standard SDK build
  const { buildMod: sdkBuild } = await import("@hotbunny/hackhub-content-sdk/build");
  await sdkBuild();

  // 5. Copy individual app.htmls to dist/ for headless preview
  for (const { entry, html } of generated) {
    const out = path.join(root, "dist", "apps", entry.name, "app.html");
    fs.mkdirSync(path.dirname(out), { recursive: true });
    fs.writeFileSync(out, html, "utf-8");
  }

  console.log(`${tag} \x1b[32mDone.\x1b[0m`);
}
